package nandcat.view;

/**
 * Adapter simplifying the use of WorkspaceListener.
 */
public abstract class WorkspaceListenerAdapter implements WorkspaceListener {

    /**
     * {@inheritDoc}
     */
    public void mouseClicked(WorkspaceEvent e) {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseReleased(WorkspaceEvent e) {
    }

    /**
     * {@inheritDoc}
     */
    public void mousePressed(WorkspaceEvent e) {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseDragged(WorkspaceEvent e) {
    }

    /**
     * {@inheritDoc}
     */
    public void mouseMoved(WorkspaceEvent e) {
    }

    /**
     * {@inheritDoc}
     */
    public void windowClosing(WorkspaceEvent e) {
    }
}
